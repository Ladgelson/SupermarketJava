    public boolean isLoginGerente() {
        JanelaLogin janelaLogin = new JanelaLogin(this, true);
        janelaLogin.setVisible(true);
        
        Funcionario funcionarioLogado = janelaLogin.getFuncionarioLogado();
        if (funcionarioLogado == null) {
            JOptionPane.showMessageDialog(null, "Usu치rio/senha incorretos", "Acesso negado", JOptionPane.ERROR_MESSAGE);
            return false;
        }
        if (!funcionarioLogado.getIsGerente()) {
            JOptionPane.showMessageDialog(null, "Somente o gerente pode ter acesso", "Acesso negado", JOptionPane.ERROR_MESSAGE);
            return false;
        }
        
        return true;
    }
