        // S칩 funcionar치 se tiver encerrado uma venda;
        public void atualizarTabela() {
        DefaultTableModel caixa1 = (DefaultTableModel) tbCaixa1.getModel();
        DefaultTableModel caixa2 = (DefaultTableModel) tbCaixa2.getModel();
        DefaultTableModel caixa3 = (DefaultTableModel) tbCaixa3.getModel();
        caixa1.setRowCount(0); // limpando a tabela
        caixa2.setRowCount(0); // limpando a tabela
        caixa3.setRowCount(0); // limpando a tabela
        
        Double totalCaixa1 = 0D;
        Double totalCaixa2 = 0D;
        Double totalCaixa3 = 0D;
        
        for (Venda venda : Banco.vendas) {
            if (venda.getCaixa().equals("01")) {
                Object[] Dados = {venda.getFuncionario().getNome(), NumberFormat.getCurrencyInstance().format(venda.calcularSubtotal()), NumberFormat.getCurrencyInstance().format(venda.getTotalRecebido()), NumberFormat.getCurrencyInstance().format(venda.calcularTroco()), venda.getFormaPagamento()};
                 // adicionando o vetor object[] na tabela com as lista dos produtos salvos.
                 caixa1.addRow(Dados);
                 totalCaixa1 += venda.calcularSubtotal();
            }
       }
        
       for (Venda venda : Banco.vendas) {
            if (venda.getCaixa().equals("02")) {
                Object[] Dados = {venda.getFuncionario().getNome(), NumberFormat.getCurrencyInstance().format(venda.calcularSubtotal()), NumberFormat.getCurrencyInstance().format(venda.getTotalRecebido()), NumberFormat.getCurrencyInstance().format(venda.calcularTroco()), venda.getFormaPagamento()};
                 // adicionando o vetor object[] na tabela com as lista dos produtos salvos.
                 caixa2.addRow(Dados);
                 totalCaixa2 += venda.calcularSubtotal();
            }
       }
       
       for (Venda venda : Banco.vendas) {
            if (venda.getCaixa().equals("03")) {
                Object[] Dados = {venda.getFuncionario().getNome(), NumberFormat.getCurrencyInstance().format(venda.calcularSubtotal()), NumberFormat.getCurrencyInstance().format(venda.getTotalRecebido()), NumberFormat.getCurrencyInstance().format(venda.calcularTroco()), venda.getFormaPagamento()};
                 // adicionando o vetor object[] na tabela com as lista dos produtos salvos.
                 caixa3.addRow(Dados);
                 totalCaixa3 += venda.calcularSubtotal();
            }
       }

       Double totalGeral = totalCaixa1 + totalCaixa2 + totalCaixa3;
       
       labelTotalCaixa1.setText(NumberFormat.getCurrencyInstance().format(totalCaixa1));
       labelTotalCaixa2.setText(NumberFormat.getCurrencyInstance().format(totalCaixa2));
       labelTotalCaixa3.setText(NumberFormat.getCurrencyInstance().format(totalCaixa3));
       labelTotalGeral.setText(NumberFormat.getCurrencyInstance().format(totalGeral));
    }
