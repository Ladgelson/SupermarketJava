    // Fazendo o evento entre o botão salvar a partir do cod,descricao,Text,Quant e Valor para add na TableProdutos
    private void btCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btCadastrarActionPerformed
        // TODO add your handling code here:
        Estoque estoque = Banco.estoque;
        
        UnidadeProduto unidade = checkUnitario.isSelected() ? UnidadeProduto.UNITARIO : UnidadeProduto.PESO;
        
        Produto produto = new Produto(txtCod.getText(), txtDescricao.getText(), Double.parseDouble(txtValor.getText()), unidade, Double.parseDouble(txtQuant.getText()));
        estoque.adicionar(produto);
        atualizarTabela();
    }//GEN-LAST:event_btCadastrarActionPerformed

    private void btExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btExcluirActionPerformed
        // System.out.println("Linha selecionada: " + Producttable.getSelectedRow());         
        // Verificar se o usuário selecionou a tabela
        if(Producttable.getSelectedRow() != -1 ){
            DefaultTableModel tbEstoque = (DefaultTableModel) Producttable.getModel();
            
            // Pegando o codigo do produto da linha selecionada
            String codigo = (String) tbEstoque.getValueAt(Producttable.getSelectedRow(), 0);
            Estoque estoque = Banco.estoque;
            
            // Removendo o produto do estoque
            Produto produtoSelecionado = null;
            for (Produto produto : estoque.getEntrada()) {
                if (produto.getCodigo().equals(codigo)) { //Verificando se o código é igual ao que foi selecionado
                    produtoSelecionado = produto;
                }
            }            
            estoque.getEntrada().remove(produtoSelecionado);
            
        } else {
            JOptionPane.showMessageDialog(null," Selecione um produto para excluir.");
        }
        atualizarTabela();
    }//GEN-LAST:event_btExcluirActionPerformed

