    public void atualizarTabela() {
        Estoque estoque = Banco.estoque;
        // criando o modelo - tbEstoque
        DefaultTableModel tbEstoque = (DefaultTableModel) Producttable.getModel();
        tbEstoque.setRowCount(0); // limpando a tabela
        
        // Aqui está colocando todos os produtos cadastrados, tanto os novos quando os antigos
        for (Produto produto : estoque.getEntrada()) {
            Object[] Dados = {produto.getCodigo(),produto.getNome(), produto.getQtdeAtualEstoque(),produto.getPreco()};
            // Adicionando o vetor object[] na tabela com as lista dos produtos salvos
            tbEstoque.addRow(Dados);
             // Adicionando o conjunto de Dados em cada linha na tabela de estoque
        }  
    }
