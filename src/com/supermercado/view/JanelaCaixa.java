    private void btExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btExcluirActionPerformed
        // verificar se o usuário selecionou a tabela
        if(tbProtudos.getSelectedRow() != -1 ){
            DefaultTableModel tbEstoque = (DefaultTableModel) tbProtudos.getModel();
            
//            pegando o numero de item da linha selecionada
            int numero = (int) tbEstoque.getValueAt(tbProtudos.getSelectedRow(), 0);
            
//            Removendo o produto do estoque
            ItemVenda itemSelecionado = null;
            for (ItemVenda itemVendido : this.venda.getItensVendidos()) {
                if (itemVendido.getNumero() == numero) { //Verificando se o numero é igual ao que foi selecionado
                    itemSelecionado = itemVendido;
                }
            }
            
            this.venda.getItensVendidos().remove(itemSelecionado);
            atualizarTabela();
        } else {
            JOptionPane.showMessageDialog(null," Selecione um produto para excluir.");
        }
    }//GEN-LAST:event_btExcluirActionPerformed

    private void btEncerrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btEncerrarActionPerformed
        // Verificação para quando clicar no botão de encerrar e caso não tenho itens vendidos
        if (this.venda.getItensVendidos().size() == 0) {
            JOptionPane.showMessageDialog(null, "Nenhum item foi vendido", "Nenhum item vendido", JOptionPane.WARNING_MESSAGE);
            btNovo.setEnabled(true); // deixa disponível o botão NOVO
            btEncerrar.setEnabled(false); // deixa desabilitado o botão de ENCERRAR
            txtCodigo.setEditable(false); // deixa desabilitado o botão de CODIGO
            this.btExcluir.setEnabled(false); // deixa desabilitado o botão de EXCLUIR ITEM
            this.labelStatus.setText("CAIXA ENCERRADO:"); 
            return;
        }
        /* Tratamento de erro para as formas de Pagamento 1 - Dinheiro  e 2 - Cartão */
        int f = 0;
        do {
            try { // vai tentar rodar as próximas linhas basta uma da erro, que vai direto para o catch(
                f = Integer.parseInt(JOptionPane.showInputDialog(null, "Formas de Pagamento: 1 - Dinheiro, 2 - Cartão ?", "Formas de Pagamento", JOptionPane.QUESTION_MESSAGE));
                if (f != 1 && f != 2)
                    JOptionPane.showMessageDialog(null, "Informe uma forma de pagamento válida", "Forma de pagamento inválida", JOptionPane.ERROR_MESSAGE);
            } catch(Exception e) { //
                JOptionPane.showMessageDialog(null, "Informe uma forma de pagamento válida", "Forma de pagamento inválida", JOptionPane.ERROR_MESSAGE);
            }
        } while(f != 1 && f != 2);
        if (f == 1) {
            Double pagamento = 0D;
            Double subTotal = this.venda.calcularSubtotal();
            do {
                try {
                    pagamento = Double.parseDouble(JOptionPane.showInputDialog(null, "Informe o valor em dinheiro:", "Informe o valor", JOptionPane.QUESTION_MESSAGE));
                    if (pagamento < subTotal)
                        JOptionPane.showMessageDialog(null, "O valor informado é menor que o total da compra", "Valor incorreto", JOptionPane.ERROR_MESSAGE);
                } catch(Exception e) {
                    JOptionPane.showMessageDialog(null, "Valor inválido", "Valor inválido", JOptionPane.ERROR_MESSAGE);
                }
            } while(pagamento < subTotal);
            
            this.venda.setFormaPagamento(FormaPagamento.DINHEIRO);
            this.venda.setTotalRecebido(pagamento);
        }
        else {
            this.venda.setFormaPagamento(FormaPagamento.CARTAO);
            this.venda.setTotalRecebido(this.venda.calcularSubtotal());
        }
        
//      Atualiza os campos da tela o total recebido e o troco, pois os números estavam saindo com uma precisão grande
        labelTotalRecebido.setText(NumberFormat.getCurrencyInstance().format(venda.getTotalRecebido()));
        labelTroco.setText(NumberFormat.getCurrencyInstance().format(venda.calcularTroco()));
        
//      Adiciona a venda no banco de dados
        Banco.vendas.add(this.venda);
        btNovo.setEnabled(true);
        btEncerrar.setEnabled(false);
        txtCodigo.setEditable(false);
        this.btExcluir.setEnabled(false);
        this.labelStatus.setText("CAIXA ENCERRADO:");
    }//GEN-LAST:event_btEncerrarActionPerformed


