    public void adicionarProduto() {
        String codigoProduto = txtCodigo.getText();
        
        Produto produto = Banco.estoque.getProduto(codigoProduto);
        if (produto == null) {
           JOptionPane.showMessageDialog(null, "O produto não foi encontrato", "Código inválido", JOptionPane.ERROR_MESSAGE);
           return;
        }
        
        Double quantidade = 0D;
        try {
            if (produto.getUnidade().equals(UnidadeProduto.UNITARIO))
                quantidade = Double.parseDouble(JOptionPane.showInputDialog("Informe a quantidade (UN): "));
            else
                quantidade = Double.parseDouble(JOptionPane.showInputDialog("Informe o peso (KG): "));
            
            if (quantidade <= 0) {
                JOptionPane.showMessageDialog(null, "A quantidade não pode ser menor ou igual a zero", "Quantidade inválida", JOptionPane.ERROR_MESSAGE);
                return;
            }
        } catch(Exception e) {
           JOptionPane.showMessageDialog(null, "A quantidade informada é inválida", "Quantidade inválida", JOptionPane.ERROR_MESSAGE);
           return;
        };
                
        Double totalItem = quantidade * produto.getPreco();

//        Verifica se está disponivel no estoque
        if (produto.getQtdeAtualEstoque() - quantidade < 0) {
            JOptionPane.showMessageDialog(null, "Quantidade não disponível no estoque", "Não disponivel no estoque", JOptionPane.ERROR_MESSAGE);
        }
        else {
            //Atualizando a quantidade no estoque
            produto.setQtdeAtualEstoque(produto.getQtdeAtualEstoque() - quantidade);
            
//            pega o total de itens da lista + 1
            int numero = this.venda.getItensVendidos().size() + 1;
            ItemVenda itemVendido = new ItemVenda(numero, produto, quantidade);
            this.venda.getItensVendidos().add(itemVendido);
        }
        
        txtCodigo.setText("");
        atualizarTabela();
    }
    
    public void atualizarTabela() {
        // criando o modelo - tbItensVendidos
        DefaultTableModel tbItensVendidos = (DefaultTableModel) tbProtudos.getModel();
        tbItensVendidos.setRowCount(0); // limpando a tabela
        
        ItemVenda ultimoItemVendido = null;
        for (ItemVenda itemVendido : venda.getItensVendidos()) {
            Object[] Dados = {itemVendido.getNumero(), itemVendido.getProduto().getCodigo(), itemVendido.getProduto().getNome(), itemVendido.getQuantidade(), NumberFormat.getCurrencyInstance().format(itemVendido.getProduto().getPreco()), NumberFormat.getCurrencyInstance().format(itemVendido.calcularTotal())};
            // adicionando o vetor object[] na tabela com as lista dos produtos salvos.
            tbItensVendidos.addRow(Dados);
            ultimoItemVendido = itemVendido;
        }
        
        if (ultimoItemVendido != null) {
            labelCodigo.setText(ultimoItemVendido.getProduto().getCodigo());
            labelValorUnitario.setText(NumberFormat.getCurrencyInstance().format(ultimoItemVendido.getProduto().getPreco()));
            labelValorTotalItem.setText(NumberFormat.getCurrencyInstance().format(ultimoItemVendido.calcularTotal()));
            labelSubtotal.setText(NumberFormat.getCurrencyInstance().format(venda.calcularSubtotal()));
            labelImagem.setIcon(new ImageIcon(getClass().getResource("/com/supermercado/resource/products/" + ultimoItemVendido.getProduto().getFoto())));
        }
        else { //Caso não tenha nenhum item vendido
            labelCodigo.setText("0");
            labelValorUnitario.setText(NumberFormat.getCurrencyInstance().format(0D));
            labelValorTotalItem.setText(NumberFormat.getCurrencyInstance().format(0D));
            labelSubtotal.setText(NumberFormat.getCurrencyInstance().format(0D));
            labelTotalRecebido.setText(NumberFormat.getCurrencyInstance().format(0D));
            labelTroco.setText(NumberFormat.getCurrencyInstance().format(0D));
            labelImagem.setIcon(new ImageIcon(getClass().getResource("/com/supermercado/resource/shopping-bag.png")));
        }
        
    }
    
    public boolean isFuncionarioLogado() {
        if (this.funcionarioLogado != null)
             return true;
        
        JanelaLogin janelaLogin = new JanelaLogin(this, true);
        janelaLogin.setVisible(true);
        
        Funcionario funcionarioLogado = janelaLogin.getFuncionarioLogado();
        if (funcionarioLogado == null) {
            JOptionPane.showMessageDialog(null, "Usuário/senha incorretos", "Acesso negado", JOptionPane.ERROR_MESSAGE);
            return false;
        }
        
        this.funcionarioLogado = funcionarioLogado;
        this.labelNomeOperador.setText(funcionarioLogado.getNome());
        
        return true;
    }

